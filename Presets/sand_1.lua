function init()
    setName("Sand 1")
    setDesc("Sets up sand")
end

function apply()
    math.randomseed(os.time())
    addNode("cells.lua", -250, -76)
    addNode("invert.lua", -130, -76)
    addNode("directional_blur.lua", 0, -76)
    addNode("blur.lua", 140, -76)
    addNode("perlin_noise.lua", 140, 112)
    addNode("blend.lua", 260, 0)
    setParameter(0, 0, math.random(-1000, 1000))
    setParameter(0, 1, 3)
    setParameter(0, 2, 30)
    setParameter(0, 3, 40)
    setParameter(2, 0, math.random(0, 360))
    setParameter(2, 1, 30)
    setParameter(3, 0, 40)
    setParameter(4, 0, math.random(-1000, 1000))
    setParameter(4, 2, 8)
    setParameter(4, 4, 100)
    setParameter(4, 5, 8)
    addConnection(0, 0, 1, 0)
    addConnection(1, 0, 2, 0)
    addConnection(2, 0, 3, 0)
    addConnection(3, 0, 5, 0)
    addConnection(4, 0, 5, 1)
end