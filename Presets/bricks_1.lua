function init()
    setName("Bricks 1")
    setDesc("Sets up bricks")
end

function apply()
    math.randomseed(os.time())
    addNode("bricks.lua", -150, -112)
    addNode("perlin_noise.lua", -290, 112)
    addNode("perlin_noise.lua", -150, 112)
    addNode("directional_blur.lua", 0, 0)
    addNode("blend.lua", 140, 0)
    gridSize = math.random(1, 2)
    setParameter(0, 0, math.random(-1000, 1000))
    setParameter(0, 1, gridSize)
    setParameter(0, 2, math.random(1, gridSize))
    setParameter(0, 3, math.random(1, gridSize))
    setParameter(0, 4, math.random(30, 100))
    setParameter(1, 0, math.random(-1000, 1000))
    setParameter(1, 1, 6)
    setParameter(1, 2, 4)
    setParameter(2, 0, math.random(-1000, 1000))
    setParameter(2, 1, 4)
    setParameter(2, 2, 3)
    setParameter(2, 5, 6)
    setParameter(4, 0, 4)
    addConnection(0, 0, 3, 0)
    addConnection(1, 0, 3, 1)
    addConnection(2, 0, 3, 2)
    addConnection(3, 0, 4, 1)
    addConnection(0, 0, 4, 0)
end